        private static Spell Create(string name, Guid levelId, Guid schoolId, bool isRitual,
            string description, string? extraDescription, Guid costId,
            Guid? savingThrowId, bool isConcentration, string range,
            Guid durationId, Guid components, List<SpellSpellDamage> spellSpellDamages)
        {
            return new Spell
            {
                Id = Guid.NewGuid(),
                Name = name,
                LevelId = levelId,
                SchoolId = schoolId,
                IsRitual = isRitual,
                Description = description,
                ExtraDescription = extraDescription,
                CostId = costId,
                SavingThrowId = savingThrowId,
                IsConcentration = isConcentration,
                Range = range,
                DurationId = durationId,
                ComponentsId = components,
                SpellSpellDamages = spellSpellDamages
            };
        }
        private static async Task<Damage> CreateDamage(ApplicationDbContext context, string damageDice, string damageType)
        {
            var existingDamage = await context.Damages
                .FirstOrDefaultAsync(d => d.DamageDice == damageDice && d.DamageType == damageType);

            if (existingDamage != null)
            {
                return existingDamage;
            }
            var newDamage = new Damage
            {
                Id = Guid.NewGuid(),
                DamageDice = damageDice,
                DamageType = damageType
            };

            await context.Damages.AddAsync(newDamage);
            return newDamage;
        }
