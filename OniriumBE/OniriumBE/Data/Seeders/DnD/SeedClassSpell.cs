            foreach (var entry in associations)
            {
                var className = entry.Key;
                var spellList = entry.Value;

                var cls = await context.Classes.FirstOrDefaultAsync(c => c.Name == className);
                if (cls == null)
                {
                    Console.WriteLine($"Classe non trovata: {className}");
                    continue;
                }

                foreach (var (spellName, spellLevel) in spellList)
                {
                    int requiredLevel = GetRequiredClassLevel(spellLevel, className);
                    if (requiredLevel == -1)
                    {
                        Console.WriteLine($"Classe {className} non può avere incantesimo di livello {spellLevel}: {spellName}");
                        continue;
                    }

                    var spell = await context.Spells.FirstOrDefaultAsync(s => s.Name == spellName);
                    if (spell == null)
                    {
                        Console.WriteLine($"Incantesimo non trovato: {spellName}");
                        continue;
                    }

                    bool alreadyLinked = await context.ClassSpells.AnyAsync(cs => cs.ClassId == cls.Id && cs.SpellId == spell.Id);
                    if (!alreadyLinked)
                    {
                        context.ClassSpells.Add(new ClassSpell
                        {
                            Id = Guid.NewGuid(),
                            ClassId = cls.Id,
                            SpellId = spell.Id,
                            RequiredClassLevel = requiredLevel
                        });
                    }
                }

                await context.SaveChangesAsync();
            }



        }

        private static int GetRequiredClassLevel(int spellLevel, string className)
        {
            if (spellLevel == 0) return 1;

            var fullCasters = new[] { "Mago", "Chierico", "Druido", "Bardo", "Stregone", "Warlock" };
            var halfCasters = new[] { "Paladino", "Ranger" };
            var halfPlusCasters = new[] { "Artefice" };

            if (fullCasters.Contains(className))
            {
                return spellLevel * 2 - 1;
            }
            else if (halfCasters.Contains(className))
            {
                if (spellLevel > 5) return -1;
                return spellLevel switch
                {
                    1 => 2,
                    2 => 5,
                    3 => 9,
                    4 => 13,
                    5 => 17,
                    _ => -1
                };
            }
            else if (halfPlusCasters.Contains(className))
            {
                // Artificer ha slot da livello 1 ma arriva solo fino al 5° livello
                if (spellLevel > 5) return -1;  // Paladin/Ranger arrivano fino al 5° livello di incantesimi
                return spellLevel * 2 - 1;
            }

            // Unknown class
            return -1;
        }
